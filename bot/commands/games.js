// Вспомогательные функции для игры в города
function startCitiesGame(bot, state, sender) {
    state.citiesGame = {
        active: true,
        player: sender,
        usedCities: [],
        lastLetter: null,
        timer: null,
        playerScore: 0,
        botScore: 0,
        startTime: Date.now(),
        timeLimit: 5 * 60 * 1000
    };

    bot.chat(`/cc &b${sender}&f нᴀчинᴀᴇᴛ игру ʙ городᴀ! нᴀᴨиɯи любой город роᴄᴄии (нᴀприʍᴇр: #городᴀ ʍоᴄᴋʙᴀ)`);

    state.citiesGame.timer = setTimeout(() => {
        endCitiesGame(bot, state, 'time');
    }, state.citiesGame.timeLimit);
}

function handlePlayerCity(bot, state, player, cityInput) {
    const game = state.citiesGame;
    if (Date.now() - game.startTime > game.timeLimit) {
        endCitiesGame(bot, state, 'time');
        return;
    }

    const city = cityInput.charAt(0).toUpperCase() + cityInput.slice(1).toLowerCase();

    if (!CITIES.includes(city)) {
        bot.chat(`/cc &b${player}&f, городᴀ "&c${city}&f" нᴇᴛ ʙ ᴄпиᴄᴋᴇ!`);
        return;
    }

    if (game.usedCities.includes(city)) {
        bot.chat(`/cc &b${player}&f, город "&c${city}&f" ужᴇ быᴧ!`);
        return;
    }

    if (game.lastLetter && city.charAt(0).toLowerCase() !== game.lastLetter) {
        bot.chat(`/cc &b${player}&f, нужно город нᴀ буᴋʙу "${game.lastLetter.toUpperCase()}"!`);
        return;
    }

    game.usedCities.push(city);
    game.playerScore++;

    let lastLetter = getLastValidLetter(city);
    game.lastLetter = lastLetter;

    bot.chat(`/cc &b${player}&f: &a${city}&f -> ᴄлᴇдующᴀя буᴋʙᴀ: "${lastLetter.toUpperCase()}"`);

    setTimeout(() => {
        makeBotMove(bot, state);
    }, 1500);
}

function getLastValidLetter(city) {
    const invalidEndings = ['ь', 'ъ', 'ы', 'й'];
    let lastChar = city.charAt(city.length - 1).toLowerCase();
    while (invalidEndings.includes(lastChar) && city.length > 1) {
        city = city.slice(0, -1);
        lastChar = city.charAt(city.length - 1).toLowerCase();
    }
    return lastChar;
}

// Завершение игры
function endCityGame(bot, state, gameId, winner) {
    const game = state.cityGames?.[gameId];
    if (!game) return;

    const loser = (game.player1 === winner) ? game.player2 : game.player1;
    bot.chat(`/cc 🏆 &a${winner}&f выигрывает! Игрок &c${loser}&f проиграл.`);

    // Сохраняем статистику
    if (!state.clanData.games) state.clanData.games = {};
    if (!state.clanData.games.cityPvP) state.clanData.games.cityPvP = {};

    if (!state.clanData.games.cityPvP[winner]) state.clanData.games.cityPvP[winner] = { wins: 0, losses: 0 };
    state.clanData.games.cityPvP[winner].wins++;

    if (loser !== 'Бот') { // боту статистику не сохраняем
        if (!state.clanData.games.cityPvP[loser]) state.clanData.games.cityPvP[loser] = { wins: 0, losses: 0 };
        state.clanData.games.cityPvP[loser].losses++;
    }

    saveData(state.clanData, state.config.dataFile);
    clearTimeout(game.timer);
    delete state.cityGames[gameId];
}

// Ход бота
async function botMove(bot, state, gameId) {
    const game = state.cityGames[gameId];
    if (!game || game.turn !== 'Бот' || game.status !== 'playing' || !game.botMode) return;

    const available = CITIES.filter(city =>
    !game.usedCities.includes(city) &&
    city.charAt(0).toLowerCase() === game.lastLetter
    );

    if (available.length === 0) {
        // Бот не может ходить – выигрывает игрок
        endCityGame(bot, state, gameId, game.player1);
        return;
    }

    const botCity = available[Math.floor(Math.random() * available.length)];
    game.usedCities.push(botCity);
    const lastLetter = getLastValidLetter(botCity);
    game.lastLetter = lastLetter;

    bot.chat(`/cc &fБот: &a${botCity}&f -> буква "${lastLetter.toUpperCase()}". Ходит &a${game.player1}&f.`);

    game.turn = game.player1;

    // Проверка, может ли игрок ходить
    const playerCanMove = CITIES.some(city =>
    !game.usedCities.includes(city) &&
    city.charAt(0).toLowerCase() === lastLetter
    );
    if (!playerCanMove) {
        endCityGame(bot, state, gameId, 'Бот');
    }
}

